public class Lab035 {
    public static void main(String[] args) {
        char c = '\n'; // New line / Next Line
        char c1 = '\t'; // Add Tab
        char c2 = '\b'; // Backlash, delete 1 char
        char c3 = '\r'; // Backlash, delete 1 word
        System.out.println("Harsh" + c3 + "raj");
        System.out.println("Harsh" + c2 + "Raj");
        System.out.println("Harsh" + c1 + "Raj");
        System.out.println("Harsh" + c + "Raj");
    }
}
